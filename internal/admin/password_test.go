func TestResetPasswordRegenKeys(t *testing.T) {
	p := newKeyedUser(t)

	before, err := p.UserKeyStatus("example.com", "alice")
	if err != nil || !before.Exists {
		t.Fatalf("key status before: %+v err=%v", before, err)
	}

	fingerprint, err := p.ResetPasswordRegenKeys("example.com", "alice", "new-password-456")
	if err != nil {
		t.Fatalf("ResetPasswordRegenKeys: %v", err)
	}
	if fingerprint == "" {
		t.Fatal("want new fingerprint for keyed user")
	}
	if fingerprint == before.Fingerprint {
		t.Error("fingerprint unchanged -- keypair must be regenerated, not reused")
	}

	// New password authenticates and unseals the NEW key. No lockout.
	session, err := authenticate(t, p, "example.com", "alice", "new-password-456")
	if err != nil {
		t.Fatalf("authenticate after regen: %v", err)
	}
	defer session.Clear()
	if !session.EncryptionEnabled || len(session.PrivateKey) != 32 {
		t.Fatal("regenerated key did not unseal at login")
	}
}

func TestResetPasswordRegenKeys_KeylessUser(t *testing.T) {
	// A keyless user gets a plain hash reset; no keypair is invented.
	p := newTestPaths(t)
	if _, err := p.CreateDomain("example.com"); err != nil {
		t.Fatal(err)
	}
	if _, err := p.CreateUser("example.com", "bob", "old-password-123", false); err != nil {
		t.Fatal(err)
	}

	fingerprint, err := p.ResetPasswordRegenKeys("example.com", "bob", "new-password-456")
	if err != nil {
		t.Fatalf("ResetPasswordRegenKeys keyless: %v", err)
	}
	if fingerprint != "" {
		t.Errorf("keyless user must not gain keys from a password reset, got fingerprint %q", fingerprint)
	}
	status, _ := p.UserKeyStatus("example.com", "bob")
	if status.Exists {
		t.Error("keypair fabricated for keyless user")
	}
}
