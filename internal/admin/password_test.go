func TestResetPassword_RefusesKeyedUser(t *testing.T) {
	p := newKeyedUser(t)

	err := p.ResetPassword("example.com", "alice", "new-password-456")
	if !errors.Is(err, ErrUserHasKeys) {
		t.Fatalf("ResetPassword on keyed user: want ErrUserHasKeys, got %v", err)
	}

	// Refusal must not have touched the hash.
	session, err := authenticate(t, p, "example.com", "alice", "old-password-123")
	if err != nil {
		t.Fatalf("original password broken after refused reset: %v", err)
	}
	session.Clear()
}

